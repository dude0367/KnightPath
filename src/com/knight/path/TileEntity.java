package com.knight.path;

import java.awt.Color;

public class TileEntity {
	
	private int x, y;
	private Color color;
	
	public TileEntity() {
		
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public Color getColor() {
		return color;
	}

	public void setColor(Color color) {
		this.color = color;
	}

}
